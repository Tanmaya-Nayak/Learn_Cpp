#include <iostream>
using namespace std;

int main() {
  // Binary Operators

  int a = 20, b = 10;
  cout << a + b << endl;
  cout << a - b << endl;
  cout << a * b << endl;
  cout << a / b << endl;
  cout << a % b << endl;

  // Unary Operators
  int c = 1;
  c++;
  cout << c;
  c--;
  cout << c;
}
